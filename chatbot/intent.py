def is_similar(a, b):
    # ปรับเพิ่มความยืดหยุ่นในการหาคำ ถ้าคำสั้นให้เช็คตรงๆ
    if len(b) <= 3:
        return a.find(b) != -1
    return fuzz.partial_ratio(a, b) > 75 

def detect_intents(message):
    message = message.lower()
    found = []
    for intent, keywords in intents.items():
        for word in keywords:
            if word in message or is_similar(message, word):
                if intent not in found:
                    found.append(intent)
                break
    return found if found else ["unknown"]