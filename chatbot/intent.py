def is_similar(a, b):
    return fuzz.partial_ratio(a, b) > 70 

def detect_intents(message):
    message = message.lower()
    found = []
    for intent, keywords in intents.items():
        for word in keywords:
            if is_similar(message, word):
                found.append(intent)
                break
    return found if found else ["unknown"]